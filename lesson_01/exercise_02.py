# 2. По введенным пользователем координатам двух точек вывести уравнение прямой вида y = kx + b,
# проходящей через эти точки.

x1: int = int(input('Введите кординату x1: '))
y1: int = int(input('Введите кординату y1: '))
x2: int = int(input('Введите кординату x2: '))
y2: int = int(input('Введите кординату y2: '))

k: float = (y2 - y1) / (x2 - x1)
b: float = y1 - k * x1

if __name__ == '__main__':
    print(f'Уравнение прямой: y = {k}x + {b}')
